package basico1;

public class ComparaçãoEntreDoisNumeros {

	
	public static void main(String[] args) {
		
		int p1 = 25;
		int p2 = 39;
		
		if (p1 == 39) {
			System.out.printf("%d é igual a %d\n", p1, p2);	
			
		}if (p1 != p2) {
			System.out.printf("%d é deferente de %d\n", p1, p2);
			
		}if (p1 < p2) {
			System.out.printf("%d é menor de %d\n", p1, p2);
					
		}if (p1 > p2) {
			System.out.printf("%d é maior de %d", p1, p2);
			
		}
	}
}
